def format_text(text):
    return (''.join([c for c in unicodedata.normalize('NFD', text) if not unicodedata.combining(c)])).lower()

def is_house(text, house_type):
    words = format_text(text)
    if 'byt' in words or 'garzonk' in words:
        return None

    for word in house_type:
        if word in words and 'byt' not in words:
            if word == 'rd' or word == 'rdv' or word == 'bungalov':
                return 'dom'
            elif word == 'chalupa':
                return 'chat'
            return word
    return None



def find_area_sizes(text, keyword):
    found_sizes = []

    pattern = r"\d+(?:[.,]\d+)?" + re.escape(keyword)

    tmp = text.replace('\u00A0', '')
    matches = re.finditer(pattern, tmp.replace(' ', ''))
    for match in matches:
        found_sizes.append(match.group())

    return found_sizes


def find_area(text, area_def):
    keys = ['short_text', 'full_text', 'title']
    found = False
    found_sizes = set()
    found_size_type = None

    for size_type in area_def:
        for key in keys:
            formated_text = format_text(text[key])
            if size_type in formated_text:
                found = True
                found_sizes.update(find_area_sizes(formated_text, size_type))
        if found:
            found_size_type = size_type
            break

    if not found or found_sizes == set(): return None

    tmp = set()
    for size in found_sizes:
        if (',' in size):
            size = size.replace(',', '.')
        tmp.add(float(size.replace(found_size_type, '')))

    print(max(tmp))

    if found_size_type == 'arov':
        return (max(tmp)*100, found_size_type)
    return (max(tmp), found_size_type)

